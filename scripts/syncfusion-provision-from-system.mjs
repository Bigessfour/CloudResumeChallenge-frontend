/**
 * Read license from Windows User/Machine env and write recommended locations:
 * - syncfusion-license.txt (Syncfusion official file method)
 * - .env (project local, gitignored)
 * Then run syncfusion-license-from-env.mjs
 */
import { execSync, spawnSync } from "node:child_process";
import { writeFileSync } from "node:fs";

function readWindowsEnv(name) {
  try {
    const ps = `[Environment]::GetEnvironmentVariable('${name}','Machine')`;
    let value = execSync(`powershell -NoProfile -Command "${ps}"`, {
      encoding: "utf8",
      windowsHide: true,
    }).trim();
    if (value) return value;
    const psUser = `[Environment]::GetEnvironmentVariable('${name}','User')`;
    value = execSync(`powershell -NoProfile -Command "${psUser}"`, {
      encoding: "utf8",
      windowsHide: true,
    }).trim();
    return value || null;
  } catch {
    return null;
  }
}

const key =
  readWindowsEnv("SYNCFUSION_LICENSE_KEY") ||
  readWindowsEnv("SYNCFUSION_LICENSE") ||
  process.env.SYNCFUSION_LICENSE_KEY?.trim() ||
  process.env.SYNCFUSION_LICENSE?.trim();

if (!key) {
  console.error(
    "No Syncfusion license in Windows User/Machine env or current process.\n" +
      "Set SYNCFUSION_LICENSE_KEY or SYNCFUSION_LICENSE, then restart Cursor.",
  );
  process.exit(1);
}

if (key.length < 20) {
  console.error("License key looks too short. Check the system variable value.");
  process.exit(1);
}

// Official file location (priority over env per Syncfusion docs)
writeFileSync("syncfusion-license.txt", key + "\n", "utf8");
console.log("Wrote syncfusion-license.txt (gitignored).");

// Project .env — official name SYNCFUSION_LICENSE + legacy alias
const envContent = [
  "# Generated by npm run syncfusion:provision — do not commit",
  "# https://ej2.syncfusion.com/documentation/licensing/license-key-registration",
  `SYNCFUSION_LICENSE=${key}`,
  `SYNCFUSION_LICENSE_KEY=${key}`,
  "",
].join("\n");
writeFileSync(".env", envContent, "utf8");
console.log("Wrote .env (gitignored).");

process.env.SYNCFUSION_LICENSE = key;
process.env.SYNCFUSION_LICENSE_KEY = key;

console.log("Generating js/syncfusion-license.js …");
const gen = spawnSync("node", ["scripts/syncfusion-license-from-env.mjs"], {
  stdio: "inherit",
  env: process.env,
  shell: false,
});

if (gen.status !== 0) {
  process.exit(gen.status ?? 1);
}

console.log("Running npx syncfusion-license activate …");
const activate = spawnSync("npx", ["syncfusion-license", "activate"], {
  stdio: "inherit",
  env: process.env,
  shell: true,
});

if (activate.status !== 0) {
  console.warn(
    "npx syncfusion-license activate skipped or failed (optional for static CDN).\n" +
      "CDN runtime uses js/syncfusion-license.js + ej.base.registerLicense — already generated.",
  );
}

process.exit(0);
